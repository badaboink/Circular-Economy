package com.circular.Circular.economy.dto.geocoding;

public class Location {
    private float lat;
    private float lng;

    // getters and setters

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }

    public float getLng() {
        return lng;
    }

    public void setLng(float lng) {
        this.lng = lng;
    }
}